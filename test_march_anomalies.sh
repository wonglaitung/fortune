#!/bin/bash
# 批量测试3月15日到30日的股票异常

echo "开始测试3月15日到30日的股票异常检测..."
echo "=============================================="

# 生成日期列表
for date in {2026-03-15..2026-03-30}; do
    echo ""
    echo "=============================================="
    echo "测试日期: $date"
    echo "=============================================="

    # 使用深度分析模式测试指定日期的异常
    python3 detect_stock_anomalies.py \
        --mode standalone \
        --mode-type deep \
        --time-interval day \
        --date "$date" \
        --no-email \
        --verbose

    echo ""
done

echo ""
echo "=============================================="
echo "测试完成！"
echo "=============================================="
